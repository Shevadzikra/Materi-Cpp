#include <iostream>
using namespace std;

int main() {

    int a;
    int b;

    a = 5;
    b = 10;

    // menghasilkan output berupa boolean.
    // 1 = true. 0 = false

    // lebih kecil dari
    cout << (a < b) << endl;

    // lebih besar dari
    cout << (a > b) << endl;

    // sama dengan dari
    cout << (a == b) << endl;

    // tidak sama dengan dari
    cout << (a != b) << endl;

    // lebih kecil atau sama dengan dari
    cout << (a <= b) << endl;

    // lebih besar atau sama dengan dari
    cout << (a >= b) << endl;

    return 0;
}